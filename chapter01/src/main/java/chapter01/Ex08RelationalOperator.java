package chapter01;

public class Ex08RelationalOperator {
	public static void main(String[] args) {
		// 관계 연산자: ==, i=, >, <, >=, <=
		
		// boolean result = 'a' > 'b';
		
		System.out.println('a' > 'b');
		System.out.println(-1 < 0);
		System.out.println(3.45 <= 2);
		System.out.println(3 != 2);
		System.out.println(3 == 2 + 1);
		System.out.println(!(3 == 2));
	}
}
